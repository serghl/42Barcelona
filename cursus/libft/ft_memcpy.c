#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, int n)
{
	int	i;	

	i = 0;
	if (!dst && !src)
		return (0);
	while (i < n)
	{
		((char *)dst)[i] = ((char *)src)[i];
		i++;
	}
	return (dst);
}
